    private void btnEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditMouseClicked
        
//        String query = String.format("SELECT ((Select tanggalWaktuPengajuan from pengajuan where idPengajuan = %s)>CURRENT_DATE)", this.idPengajuan);
        String query = String.format("INSERT INTO `daftar_permintaan_update` (idPengajuan, idRuang,namaLembaga ,tanggalWaktuPeminjaman, waktuAwalPeminjaman, waktuAkhirPeminjaman) VALUES (%s, %s, '%s', '%s', %s, %s)", idPengajuan, idRuang, tfNamaLembaga.getText(), tfDate.getText(), tfAwal.getText(), tfAkhir.getText());
        int awal = Integer.parseInt(tfAwal.getText());
        int akhir = Integer.parseInt(tfAkhir.getText());
        try{
            if(awal <7){
                JOptionPane.showMessageDialog(null, "Waktu mulai kegiatan terlalu awal");
            }else if(akhir>21){
                JOptionPane.showMessageDialog(null, "Waktu kegiatan berakhir terlalu malam");
            }else if(awal>=akhir){
                JOptionPane.showMessageDialog(null, "Waktu kegiatan berakhir tidak boleh mendahului waktu kegiatan mulai");
            }else{
                updateManager.insertUpdatePengajuan(query);
                JOptionPane.showMessageDialog(null, "Perubahan berhasil diajukan");
                updateManager.showHalamanStatusPengajuan();
                this.hide();
            }
        }catch(Exception e){
            Logger.getLogger(HalamanStatusPengajuan.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(null, "Ada error");
        }
        
        
    }//GEN-LAST:event_btnEditMouseClicked

