    public void populateComboBoxRuang(){
        String query = "select * from ruang";
        comboBoxRuang.removeAllItems();
        try {
            comboBoxRuang.removeAll();
            ArrayList<Ruang> arr = updateManager.populateComboBoxRuang(query);
            
            for(Ruang r : arr){
               comboBoxRuang.addItem(r.getNamaRuang());
            }
            } catch (Exception ex) {
            Logger.getLogger(HalamanStatusPengajuan.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Ada error saat mengisi combobox");
        }
    }
